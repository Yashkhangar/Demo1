name= str(input("Enter the name of student"))
percentage = float(input("Enter the percentage of the student"))

if 100<=percentage>90:
    print("Grade A")
elif 90<=percentage>80:
    print("Grade B")
elif 80<=percentage>=60:
    print("Grade C")
elif percentage <60 :
    print("Grade D")
else :
    print("Invalid input")


















